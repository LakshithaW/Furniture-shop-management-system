    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        boolean check = validator();

        if (check == true){
            String no = txtVehicleNo.getText();
            String type = cmbType.getSelectedItem().toString();
            String brand = txtVehicleBrand.getText();
            String model = txtVehicleModel.getText();
            String description = txtDescription.getText();
            String driver = cmbDriver.getSelectedItem().toString();

            try {

                String q="INSERT INTO vehicle (vehicleNo,brand,model,vehicleDescription,vehicleType,driverID) values ('"+ no +"','"+ brand +"','"+ model +"','"+ description +"','"+ type +"', '"+ driver +"' )";

                pst =con.prepareStatement(q);

                pst.execute();

                tableload();
                reset();

            }
            catch (Exception e)
            {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        boolean check = validator();
        if (check == true)
        {
            int x = JOptionPane.showConfirmDialog(null, "Do you want to update?");

            if(x==0)
            {
                String no = txtVehicleNo.getText();
                String type = cmbType.getSelectedItem().toString();
                String brand = txtVehicleBrand.getText();
                String model = txtVehicleModel.getText();
                String description = txtDescription.getText();
                String driver = cmbDriver.getSelectedItem().toString();

                String sql = "UPDATE vehicle SET vehicleType ='"+ type +"'  , brand ='"+ brand +"'  , model ='"+ model +"'  , vehicleDescription ='"+ description +"' , driverID = '"+ driver +"' where vehicleNo = '"+ no +"' ";
                try
                {
                    pst = con.prepareStatement(sql);
                    pst.execute();

                    tableload();
                    reset();
                }
                catch(Exception e)
                {
                    System.out.println(e);
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

