    public boolean validator()
    {
        
        if (txtVehicleNo.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Vehicle number Field is Empty ");
            return false;
        }
        else if (txtVehicleBrand.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Vehicle Brand Field is Empty ");
            return false;
        }
        else if (txtVehicleModel.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Vehicle Model Field is Empty ");
            return false;
        }
        else if (txtDescription.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Description Field is Empty ");
            return false;
        }
        return true;
    } 

    
